def load_module(module_num):
    """加载模块题库"""
    filename = None
    for f in os.listdir(DATASET_DIR):
        if f.startswith(f'module_0{module_num}_') and f.endswith('.json'):
            filename = f
            break
    if not filename:
        raise FileNotFoundError(f'Module {module_num} file not found')
    path = os.path.join(DATASET_DIR, filename)
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f), path, filename


def save_module(data, path, filename):
    """保存模块题库（同时保存到 dataset/dataset 和 backend/data/dataset）"""
    # Backup original
    backup_path = os.path.join(BACKUP_DIR, filename)
    if not os.path.exists(backup_path):
        with open(backup_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    # Save to dataset/dataset
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'  Saved: {path}')

    # Also save to backend/data/dataset
    backend_path = os.path.join(BASE_DIR, 'backend', 'data', 'dataset', filename)
    if os.path.exists(os.path.dirname(backend_path)):
        with open(backend_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f'  Saved: {backend_path}')


def build_module(module_num, tf_questions):
    """构建单个模块的题库：50选择 + 50判断"""
    data, path, filename = load_module(module_num)
    meta = data.get('meta', {})
    module_name = meta.get('module', f'Module {module_num}')
    original_qa = data.get('qa_pairs', [])

    # 1. 保留现有选择题
    existing_mc = [q for q in original_qa if q.get('type') == 'multiple_choice']

    # 2. 从简答/填空题的答案中提取关键信息，转换为选择题（补充到50道）
    other_qs = [q for q in original_qa if q.get('type') not in ['multiple_choice', 'true_false']]

    new_qa_pairs = []

    # 添加现有选择题（最多50道）
    mc_to_keep = existing_mc[:50]
    new_qa_pairs.extend(mc_to_keep)

    # 如果选择题不足50道，从其他题型转换或生成新题
    if len(mc_to_keep) < 50:
        needed = 50 - len(mc_to_keep)
        # 从简答/填空题转换：提取关键事实，构造选择题
        for q in other_qs:
            if len(new_qa_pairs) >= 50:
                break
            if q.get('type') in ['fill_in_blank']:
                # 将填空题转为选择题
                new_mc = convert_fill_blank_to_mc(q)
                if new_mc:
                    new_qa_pairs.append(new_mc)

    # 如果仍然不足50道（从现有内容无法转换足够），不强制填满，保持实际数量
    mc_count = len([q for q in new_qa_pairs if q.get('type') == 'multiple_choice'])
    print(f'  Module {module_num}: {module_name}')
    print(f'    原有选择题: {len(existing_mc)}, 保留: {len(mc_to_keep)}, 最终选择: {mc_count}')

    # 3. 添加判断题（50道）
    sections_used = set()
    for q in original_qa:
        sections_used.add(q.get('section', ''))

    tf_added = 0
    for tf in tf_questions:
        if tf_added >= 50:
            break
        question_text, answer, section, difficulty = tf

        tf_item = {
            "question": question_text,
            "answer": answer,
            "options": ["正确", "错误"],
            "type": "true_false",
            "source": "generated_v2",
            "section": section,
            "difficulty": difficulty,
            "knowledge_point": section,
        }
        new_qa_pairs.append(tf_item)
        tf_added += 1

    print(f'    判断题: {tf_added}')
    print(f'    总题目数: {len(new_qa_pairs)}')

    # 更新meta
    new_data = {
        "meta": {
            "module": module_name,
            "total_qa": len(new_qa_pairs),
            "source": "rebuild_v2 — 50选择 + 50判断",
            "type_distribution": {
                "multiple_choice": mc_count,
                "true_false": tf_added,
            }
        },
        "qa_pairs": new_qa_pairs
    }

    return new_data, path, filename


def convert_fill_blank_to_mc(q):
    """将填空题转换为单选题"""
    question = q.get('question', '')
    answer = q.get('answer', '').strip()
    section = q.get('section', '')
    difficulty = q.get('difficulty', 'medium')
    knowledge_point = q.get('knowledge_point', '')

    if not answer or len(answer) < 2:
        return None

    # 简化括号中的内容作为正确答案
    import re
    # 提取括号中的内容
    bracket_match = re.search(r'[（(]([^）)]*)[）)]', question)
    if bracket_match:
        # 将填空题改写为选择题
        clean_q = re.sub(r'[（(][^）)]*[）)]', '（ ）', question)
        correct = answer

        # 生成干扰项（基于答案的简单变体）
        distractors = generate_distractors(answer, section)

        options = [correct] + distractors[:3]
        random.shuffle(options)

        return {
            "question": clean_q,
            "answer": correct,
            "options": options,
            "type": "multiple_choice",
            "source": "converted_from_fill_blank",
            "section": section,
            "difficulty": difficulty,
            "knowledge_point": knowledge_point or section,
        }

    return None


